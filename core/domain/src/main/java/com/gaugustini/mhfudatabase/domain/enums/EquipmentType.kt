package com.gaugustini.mhfudatabase.domain.enums

/**
 * Represents the equipment type.
 */
enum class EquipmentType {
    /**
     * Head armor piece.
     */
    ARMOR_HEAD,

    /**
     * Chest armor piece.
     */
    ARMOR_CHEST,

    /**
     * Arms armor piece.
     */
    ARMOR_ARMS,

    /**
     * Waist armor piece.
     */
    ARMOR_WAIST,

    /**
     * Legs armor piece.
     */
    ARMOR_LEGS,

    /**
     * Decoration (jewel).
     */
    DECORATION,

    /**
     * Weapon.
     */
    WEAPON;
}
