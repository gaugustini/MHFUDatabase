package com.gaugustini.mhfudatabase.domain.enums

/**
 * Represents the weapon element.
 */
enum class WeaponElement {
    /**
     * Fire element.
     */
    FIRE,

    /**
     * Water element.
     */
    WATER,

    /**
     * Thunder element.
     */
    THUNDER,

    /**
     * Ice element.
     */
    ICE,

    /**
     * Dragon element.
     */
    DRAGON,

    /**
     * Poison status.
     */
    POISON,

    /**
     * Paralysis status.
     */
    PARALYSIS,

    /**
     * Sleep status.
     */
    SLEEP;
}
